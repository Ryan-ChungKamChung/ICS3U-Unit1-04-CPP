// Program finding area and perimeter of a rectangle

#include <iostream>

int main() {
    std::cout << "With the dimensions of 3cm and 5cm," << std::endl;
    std::cout << "the area would be 3(5)cm^2" << std::endl;
    std::cout << "=" << (3*5) << "cm^2" << std::endl;
    std::cout << "With the dimensions of 3cm and 5cm," << std::endl;
    std::cout << "the perimeter would be 2(3+5)cm" << std::endl;
    std::cout << "=" << 2*(3+5) << "cm" << std::endl;
}
